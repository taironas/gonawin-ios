extension UIColor {
    static func seaFoamColor() -> UIColor {
        return UIColor(colorLiteralRed: 0.0/255.0, green: 255.0/255.0, blue: 128.0/255.0, alpha: 1.0)
    }
    
    static func lightSeaFoamColor() -> UIColor {
        return UIColor(colorLiteralRed: 76.0/255.0, green: 255.0/255.0, blue: 166.0/255.0, alpha: 1.0)
    }
    
    static func darkSeaFoamColor() -> UIColor {
        return UIColor(colorLiteralRed: 38.0/255.0, green: 127.0/255.0, blue: 83.0/255.0, alpha: 1.0)
    }
    
    static func greenSeaFoamColor() -> UIColor {
        return UIColor(colorLiteralRed: 0.0/255.0, green: 204.0/255.0, blue: 102.0/255.0, alpha: 1.0)
    }
    
    static func darkBlueColor() -> UIColor {
        return UIColor(colorLiteralRed: 82.0/255.0, green: 101.0/255.0, blue: 122.0/255.0, alpha: 1.0)
    }
}
