extension UIColor {
    static var seaFoam: UIColor {
        get {
            return UIColor(red: 0.0/255.0, green: 255.0/255.0, blue: 128.0/255.0, alpha: 1.0)
        }
    }
    
    static var lightSeaFoam: UIColor {
        get {
            return UIColor(red: 76.0/255.0, green: 255.0/255.0, blue: 166.0/255.0, alpha: 1.0)
        }
    }
    
    static var darkSeaFoam: UIColor {
        get {
            return UIColor(red: 38.0/255.0, green: 127.0/255.0, blue: 83.0/255.0, alpha: 1.0)
        }
    }
    
    static var greenSeaFoam: UIColor {
        get {
            return UIColor(red: 0.0/255.0, green: 204.0/255.0, blue: 102.0/255.0, alpha: 1.0)
        }
    }
    
    static var darkBlue: UIColor {
        get {
            return UIColor(red: 82.0/255.0, green: 101.0/255.0, blue: 122.0/255.0, alpha: 1.0)
        }
    }
    
    static var lighterGray: UIColor {
        get {
            return UIColor(red: 220.0/255.0, green:220.0/255.0, blue:220.0/255.0, alpha:1)
        }
    }
}
